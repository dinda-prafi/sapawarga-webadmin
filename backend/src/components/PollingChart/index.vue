<template>
  <div :class="className" :style="{height:height,width:width}" />
</template>

<script>
import echarts from 'echarts'
require('echarts/theme/macarons') // echarts theme
import { debounce } from '@/utils'
import { fetchResult } from '@/api/polling'

export default {
  props: {
    className: {
      type: String,
      default: 'chart'
    },
    width: {
      type: String,
      default: '100%'
    },
    height: {
      type: String,
      default: '300px'
    }
  },
  data() {
    return {
      chart: null,
      id: null,
      result: [],
    }
  },
  mounted() {
    this.id = this.$route.params && this.$route.params.id
    this.getResult()
    this.initChart()
    this.__resizeHandler = debounce(() => {
      if (this.chart) {
        this.chart.resize()
      }
    }, 100)
    window.addEventListener('resize', this.__resizeHandler)
  },
  beforeDestroy() {
    if (!this.chart) {
      return
    }
    window.removeEventListener('resize', this.__resizeHandler)
    this.chart.dispose()
    this.chart = null
  },
  methods: {
    getResult() {
      const arrayBaru = [
        { value: 320, name: 'Industries' },
        { value: 240, name: 'Technology' },
        { value: 240, name: 'Technology' },
        { value: 240, name: 'Technology' },
        { value: 240, name: 'Technology' },
      ]
      const arrayLama = []
      fetchResult(this.id).then(response => { 
        response.data.forEach(function(value, key) {
          arrayLama.push({'value': value.votes, 'name': value.answer_body})
        })
      })

      console.log(arrayBaru)
      console.log(arrayLama[0])
            
      Object.assign(this.result, arrayLama)
      // console.log(this.result)
    },
    initChart() {
      this.chart = echarts.init(this.$el, 'macarons')

      this.chart.setOption({
        tooltip: {
          trigger: 'item',
          formatter: '{a} <br/>{b} : {c} ({d}%)'
        },
        legend: {
          left: 'center',
          bottom: '10',
        //   data: ['Industries', 'Technology', 'Forex', 'Gold', 'Forecasts', 'Silver']
        },
        calculable: true,
        series: [
          {
            name: 'WEEKLY WRITE ARTICLES',
            type: 'pie',
            roseType: 'radius',
            radius: [15, 95],
            center: ['50%', '38%'],
            data: this.result,
            // data: [
            //   { value: 320, name: 'Industries' },
            //   { value: 240, name: 'Technology' },
            //   { value: 149, name: 'Forex' },
            //   { value: 100, name: 'Gold' },
            //   { value: 59, name: 'Forecasts' },
            //   { value: 100, name: 'silver' },
            //   { value: 59, name: 'Sangat memuaskan' },
            //   { value: 100, name: 'Terlalu memuaskan' },
            // ],
            animationEasing: 'cubicInOut',
            animationDuration: 2600
          }
        ]
      })
    }
  }
}
</script>
